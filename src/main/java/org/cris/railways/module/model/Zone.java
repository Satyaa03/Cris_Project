package org.cris.railways.module.model;

public class Zone {
    private String railwayZone;
    private String code;
    private String headquarters;
    private String railwayDivisions;

    public String getRailwayZone() {
        return railwayZone;
    }

    public void setRailwayZone(String railwayZone) {
        this.railwayZone = railwayZone;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getHeadquarters() {
        return headquarters;
    }

    public void setHeadquarters(String headquarters) {
        this.headquarters = headquarters;
    }

    public String getRailwayDivisions() {
        return railwayDivisions;
    }

    public void setRailwayDivisions(String railwayDivisions) {
        this.railwayDivisions = railwayDivisions;
    }
}

